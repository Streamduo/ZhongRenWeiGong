public class HomeVideoDataBean {
    private String videoId;
    private String title;
    private String copyright;
    private String copyrightUrl;
    private String videoUrl;
    private String coverUrl;
    private String isHos;
    private String transmitNum;
    private String lookNum;
    private String duration;

    public String getVideoId() {
        return videoId;
    }

    public void setVideoId(String videoId) {
        this.videoId = videoId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCopyright() {
        return copyright;
    }

    public void setCopyright(String copyright) {
        this.copyright = copyright;
    }

    public String getCopyrightUrl() {
        return copyrightUrl;
    }

    public void setCopyrightUrl(String copyrightUrl) {
        this.copyrightUrl = copyrightUrl;
    }

    public String getVideoUrl() {
        return videoUrl;
    }

    public void setVideoUrl(String videoUrl) {
        this.videoUrl = videoUrl;
    }

    public String getCoverUrl() {
        return coverUrl;
    }

    public void setCoverUrl(String coverUrl) {
        this.coverUrl = coverUrl;
    }

    public String getIsHos() {
        return isHos;
    }

    public void setIsHos(String isHos) {
        this.isHos = isHos;
    }

    public String getTransmitNum() {
        return transmitNum;
    }

    public void setTransmitNum(String transmitNum) {
        this.transmitNum = transmitNum;
    }

    public String getLookNum() {
        return lookNum;
    }

    public void setLookNum(String lookNum) {
        this.lookNum = lookNum;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }
}
